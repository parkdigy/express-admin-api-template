/********************************************************************************************************************
 * SHA1 암호화 모듈
 * ******************************************************************************************************************/
import crypto from 'crypto';

export const sha1 = {
  /********************************************************************************************************************
   * SHA1 암호화
   * @param text 암호화할 문자열
   * @returns 암호화된 문자열
   * ******************************************************************************************************************/
  encode(text: string): string {
    const sha1 = crypto.createHash('sha1');
    sha1.update(text);
    return sha1.digest('hex');
  },
};

export default sha1;
