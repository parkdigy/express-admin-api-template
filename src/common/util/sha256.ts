/********************************************************************************************************************
 * SHA256 암호화 모듈
 * ******************************************************************************************************************/
import crypto from 'crypto';

export const sha256 = {
  /********************************************************************************************************************
   * SHA256 암호화
   * @param text 암호화할 문자열
   * @returns 암호화된 문자열
   * ******************************************************************************************************************/
  encode(text: string): string {
    const sha256 = crypto.createHash('sha256');
    sha256.update(text);
    return sha256.digest('hex');
  },
};

export default sha256;
